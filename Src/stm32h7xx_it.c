int lastTick = 0;

void rotHandler(uint16_t pin, GPIO_PinState state)
{
    int tick = HAL_GetTick();
    if ((tick - lastTick) > 20) {
        lastTick = tick;

      if (pin == ROT0_CK_Pin) {
          if (state == GPIO_PIN_SET) {
            ROT_Increment(&rot0);
          } else {
            ROT_Decrement(&rot0);
          }
      }

      if (pin == ROT1_CK_Pin) {
          if (state == GPIO_PIN_SET) {
            ROT_Increment(&rot1);
          } else {
            ROT_Decrement(&rot1);
          }
      }
    }
}

void EXTI1_IRQHandler(void)
{
  GPIO_PinState state = HAL_GPIO_ReadPin(ROT0_DT_GPIO_Port, ROT0_DT_Pin);

  rotHandler(GPIO_PIN_1, state);

  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_1);
}

void EXTI2_IRQHandler(void)
{
  GPIO_PinState state = HAL_GPIO_ReadPin(ROT1_DT_GPIO_Port, ROT1_DT_Pin);

  rotHandler(GPIO_PIN_2, state);

  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_2);
}

